#include<iostream>
using namespace std;
int main(){
    int a;
    float b;
    char c;
    double d;
    bool e;
    short f;
    signed int g;
    unsigned int h;
    cout << "The size of int  "<<sizeof(a)<<endl;
    cout << "The size of float  "<<sizeof(b)<<endl;
    cout << "The size of char  "<<sizeof(c)<<endl;
    cout << "The size of double  "<<sizeof(d)<<endl;
    cout << "The size of bool  "<<sizeof(e)<<endl;
    cout << "The size of short  "<<sizeof(f)<<endl;
    cout << "The size of signedint "<<sizeof(g)<<endl;
    cout << "The size of unsignedint  "<<sizeof(h)<<endl;

    return 0;
}